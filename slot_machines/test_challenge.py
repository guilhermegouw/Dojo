import unittest

from challenge import get_plays_before_broke


class TestGetPlaysBeforeBroke(unittest.TestCase):

    def test_one_quater_all_machines_starts_with_0(self):
        self.assertEqual(get_plays_before_broke(1, 0, 0, 0), "Martha plays 1 times before going broke.")

    def test_two_quater_all_machines_starts_with_0(self):
        self.assertEqual(get_plays_before_broke(2, 0, 0, 0), "Martha plays 2 times before going broke.")

    def test_two_quater_all_machines_starts_with_0(self):
        self.assertEqual(get_plays_before_broke(2, 0, 0, 0), "Martha plays 2 times before going broke.")

    def test_3_quaters_machines_one_and_two_starts_with_0_and_three_starts_with_9(self):
        self.assertEqual(get_plays_before_broke(3, 0, 0, 9), "Martha plays 12 times before going broke.")


if __name__=="__main__":
    unittest.main()


